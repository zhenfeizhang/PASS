int64 modq(int64 a, int64 q)
{
  int64 b = a%q;
  if (b<0)
    return q+b;
  else
    return b;
}

int ntt(int64 *f_ntt, const int64 *f)
{
  uint16 i,j;
  int64 base, tmp;
  memset(f_ntt, 0, sizeof(int64)*PASS_N);

  if (PASS_N%2==0)
  {
    int64 odd,even;
    for (i=0;i<PASS_N/2;i++)
    {
      odd  = f[0];
      even = f[0];
      base = 1;
      for (j=1;j<PASS_N;j++)
      {
        base = base*PASS_ROOTS[i] % PASS_p;
        tmp = f[j]*base;
        even = even + tmp;
        if (j%2==0)
          odd = odd + tmp;
        else
          odd = odd + PASS_p - tmp;
      }
      f_ntt[i]= even % PASS_p;
      f_ntt[PASS_N-1-i] = odd % PASS_p;
    }
  }
  else
  {
    for (i=0;i<PASS_N;i++)
    {
      base = 1;
      f_ntt[i]  = f[0];
      for (j=1;j<PASS_N;j++)
      {
        base = base*PASS_ROOTS[i] % PASS_p;
        f_ntt[i] += f[j]*base;

      }
      f_ntt[i] %= PASS_p;
    }
  }
  poly_cmod(f_ntt);
  return 0;
}
